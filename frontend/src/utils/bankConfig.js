/**
 * Retourne la config visuelle d'une banque selon le nom du DAB.
 * @param {string} nom
 * @returns {{ abbr, bg, text, label, key, logoUrl } | null}
 */
export const getBankConfig = (nom) => {
  if (!nom) return null;
  return BANK_CONFIGS.find((b) => b.match.test(nom)) || null;
};
